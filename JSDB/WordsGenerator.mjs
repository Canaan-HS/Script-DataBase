import open from "fs";

// 輸出文件
function GenerateWords(Data, Save="Words.json") {
    open.writeFile(
        Save,
        JSON.stringify(Data, null, 4),
    err => {
        err ? console.log("輸出失敗") : console.log("輸出成功");
    });
};

/**
 * @param {Array} Data - 數據需要是 [{}, {}...] 的格式
 * @param {boolean} Sort - 是否進行排序
 * @param {boolean} Merge - 是否合併輸出 (合併輸出就會是 Words.json)
 * @param {boolean} LengthSort - 是否使用長度來排序, 否的話使用 字母
 */
function DataCleaning({
    Data,
    Sort=true,
    Merge=false,
    LengthSort=true,
}={}) {
    const Index_Box = {};

    if (Merge) {
        const Cache = {};
        for (const D of Data) Object.assign(Cache, D);
        Data = [Cache];
    };

    Data.forEach((data, index)=> {
        const Cache = {}, Sort_Cache = [];

        // 依序取出所有物件, 將所有字串前後空白清除, 並將 key 值全部轉為小寫
        Object.entries(data).forEach(([key, value])=> {
            const clean_key = key.trim().toLowerCase();
            const clean_value = value.trim();

            Sort
            ? Sort_Cache.push({
                data: { [clean_key]: clean_value },
                length: (clean_key + clean_value).length,
            })
            : Cache[clean_key] = clean_value;
        });

        if (Sort) {
            LengthSort
            ? Sort_Cache.sort((a, b) => a.length - b.length) // 長度排序
            : Sort_Cache.sort((a, b) => Object.keys(a.data)[0].localeCompare(Object.keys(b.data)[0])); // 單字排序

            Sort_Cache.forEach(item => {
                Object.assign(Cache, item.data);
            })
        }

        Merge
        ? Object.assign(Index_Box, Cache)
        : Index_Box[index] = Cache;
    });

    if (Merge) {
        GenerateWords(Index_Box);
        return;
    }

    let start = Data.length - 1;
    for (; start >= 0; start--) { // 由最後添加的物件, 依序向自身前面添加的物件進行重複 鍵值刪除
        const compareKeys = Object.keys(Index_Box[start]);

        for (let inside = start - 1; inside >= 0; inside--) {
            compareKeys.forEach(key => {
                delete Index_Box[inside][key];
            });
        }
    };

    GenerateWords(Index_Box, "R:/Cleaning.json");
};

